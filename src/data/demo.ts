export const shotWeights: Record<Shot['status'], number> = {
  DRAFT: 0,
  READY: 0.05,
  QUEUED: 0.1,
  GENERATING: 0.46,
  GENERATED: 0.75,
  PENDING_REVIEW: 0.9,
  APPROVED: 1,
  FAILED: 0,
  BLOCKED: 0,
}

export function calculateProgress(items: Shot[]): number {
  if (items.length === 0) return 0
  const total = items.reduce((sum, shot) => sum + shotWeights[shot.status], 0)
  return Math.round((total / items.length) * 100)
}
